# Média simples
x = 10
y = 4
print ("Os números são:", x, "e", y)
ms = (x + y) / 2
print ("Média simples:", ms)

# Média harmônica
mh = 2 / (1/x + 1/y)
print ("Média harmônica:", mh)

# Média ponderada
p1 = 2
p2 = 4
print ("Os pesos são:", p1, "e", p2)
mp = (p1 * x + p2 * y) / (p1 + p2)
print ("Média ponderada:", mp)